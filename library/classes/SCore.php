        }

        $this->db = new mysqlDatabase();
        $this->db->open_connection();
        return $this->db;
    }

    //check if database connected
    function database_connected() {
        if (isset($this->db)) {
            return true;
        } else {
            return false;
        }
    }

    //close database
    function database_close() {
        $this->db->close_connection();
